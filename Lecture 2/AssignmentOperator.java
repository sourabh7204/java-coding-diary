public class AssignmentOperator {
    public static void main(String[] args) {
        int a=10;

        // a=a+10;

        //Addition
        a+=10;
        System.out.println( a);

        //Sub
        a-=5;
        System.out.println( a);


        //Mul
        a*=5;
        System.out.println( a);

        //Div
        a/=5;
        System.out.println( a);
    }   
}
