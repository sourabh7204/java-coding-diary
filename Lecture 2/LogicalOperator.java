public class LogicalOperator {
    public static void main(String[] args) {
        int a=10;
        int b=4;

        //Logical AND (&&)
        System.out.println( (a>b) && (a<b));

        //Logical OR (||)
          System.out.println( (a>b) || (a<b));

          //Logical NOT (!)
            System.out.println( !(a<b));
    }
}
