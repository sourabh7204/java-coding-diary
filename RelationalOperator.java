public class RelationalOperator {
    public static void main(String[] args) {
        int A=10;
        int B=5;
        
        //Equal to 
        System.out.println(A==B);
        //Not Equal to 
        System.out.println(A!=B);
        //Greater Than
        System.out.println(A>B);
        //Lesser Than
        System.out.println(A<B);
        ////Greater than and equal to
        System.out.println(A>=B); //if any one condition is true the op will be true orelse op will be false
        //Lesser than and equal to
        System.out.println(A<=B);
    }
}
